#include "Graphics/Material.h"



//----------------------------------------------------------------------------//
// Constructors                                             Material::Channel //
//----------------------------------------------------------------------------//

////////////////////////////////////////////////////////////////////////////////
/// <summary> </summary>

Material::Channel::Channel (void)
{
	Color.R = 0.0f;
	Color.G = 0.0f;
	Color.B = 0.0f;
	Color.A = 1.0f;

	Texture = -1;
}

////////////////////////////////////////////////////////////////////////////////
/// <summary> </summary>

Material::Channel::Channel (float r, float g, float b, float a)
{
	Color.R = r;
	Color.G = g;
	Color.B = b;
	Color.A = a;

	Texture = -1;
}



//----------------------------------------------------------------------------//
// Constructors                                                      Material //
//----------------------------------------------------------------------------//

////////////////////////////////////////////////////////////////////////////////
/// <summary> </summary>

Material::Material (void)
{
	Ambient.Color.R		= 0.2f;
	Ambient.Color.G		= 0.2f;
	Ambient.Color.B		= 0.2f;

	Diffuse.Color.R		= 0.5f;
	Diffuse.Color.G		= 0.5f;
	Diffuse.Color.B		= 0.5f;

	Specular.Color.R	= 0.4f;
	Specular.Color.G	= 0.4f;
	Specular.Color.B	= 0.4f;

	Alpha				= 1.0f;
	Shininess			= 12.0f;
	Normal				= -1;
}
