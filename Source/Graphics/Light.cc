/// <summary> </summary>

Light::Light (const Vector3& position)
{
	Position	= position;
	Visible		= true;

	Diffuse		= Color (0.8f, 0.8f, 0.8f);
	Ambient		= Color (0.8f, 0.8f, 0.8f);
	Specular	= Color (0.8f, 0.8f, 0.8f);

	Attenuation	= 45.0f;
	Exponent	= 12.0f;
}
